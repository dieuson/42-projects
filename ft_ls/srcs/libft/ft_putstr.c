#include "libft.h"
#include <unistd.h>

void	ft_putstr(char const *s)
{
	int i;

	i = 0;
	if (!s)
		write(1, "(null)", 6);
	else
	{
		while (s[i])
		{
			write(1, &s[i], 1);
			i++;
		}	
	}
}
